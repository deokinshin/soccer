package vo;

public class League {

	private int leagueNo;
	private String name;
	
	public League () {}
	
	public int getLeagueNo() {
		return leagueNo;
	}
	public void setLeagueNo(int leagueNo) {
		this.leagueNo = leagueNo;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
