package vo;

import java.util.Date;

public class Goal {

	private int playerNo;
	private String playerName;
	private Date playerBirth;
	private String playerNationality;
	private int playerGoal;
	private int clubNo;
	private String clubName;
	private int leagueNo;
	private String leagueName;
	
	public Goal () {}
	
	public int getPlayerNo() {
		return playerNo;
	}
	public void setPlayerNo(int playerNo) {
		this.playerNo = playerNo;
	}
	public String getPlayerName() {
		return playerName;
	}
	public void setPlayerName(String playerName) {
		this.playerName = playerName;
	}
	public Date getPlayerBirth() {
		return playerBirth;
	}
	public void setPlayerBirth(Date playerBirth) {
		this.playerBirth = playerBirth;
	}
	public String getPlayerNationality() {
		return playerNationality;
	}
	public void setPlayerNationality(String playerNationality) {
		this.playerNationality = playerNationality;
	}
	public int getPlayerGoal() {
		return playerGoal;
	}
	public void setPlayerGoal(int playerGoal) {
		this.playerGoal = playerGoal;
	}
	public int getClubNo() {
		return clubNo;
	}
	public void setClubNo(int clubNo) {
		this.clubNo = clubNo;
	}
	public String getClubName() {
		return clubName;
	}
	public void setClubName(String clubName) {
		this.clubName = clubName;
	}
	public int getLeagueNo() {
		return leagueNo;
	}
	public void setLeagueNo(int leagueNo) {
		this.leagueNo = leagueNo;
	}
	public String getLeagueName() {
		return leagueName;
	}
	public void setLeagueName(String leagueName) {
		this.leagueName = leagueName;
	}
	
	
}
